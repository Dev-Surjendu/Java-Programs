class lastIndexOf{
    public static void main(String args[]) {
        
        String name="Rahul_Rahul";
        System.out.println(name.lastIndexOf('h'));
        System.out.println(name.lastIndexOf("hul"));
        System.out.println(name.lastIndexOf("hul",4));
        System.out.println(name.lastIndexOf('R',7));
        
    }    
}