
class SubString{
    public static void main(String args[]) {
        
        String name="RAHUL";
        System.out.println(name.substring(3));
        System.out.println(name.substring(2,4));   // substring(start,end-1)  
        System.out.println(name.substring(2,5));   // substring(start,end-1)
    }    
}