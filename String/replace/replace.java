
class replace{
    public static void main(String args[]) {
        
        String name="Rahul";
        System.out.println(name.replace('h','l'));          // old char, new char
        System.out.println(name.replace("h","llll"));
        System.out.println(name.replace("Rahul","Surjo"));  //target ,replacement
        System.out.println(name);
    }    
}