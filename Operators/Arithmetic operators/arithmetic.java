public class arithmetic {

    public static void main(String args[]) {

        int n=6;
        int m=4;

       
        System.out.println(n+m);           // int r1 = n+m ;
        System.out.println(n-m);           // int r2 = n-m ;
        System.out.println(n*m);           // int r3 = n*m ;
        System.out.println((double)n/m);   // double r4 = (double)n/m ;
        System.out.println(n%m);           // int r5 = n%m ;
    }

}